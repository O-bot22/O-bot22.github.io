// For every page, add a button at the top to toggle between spanish and english
// also, translate the mastheaders
// also, translate anything that is in the translatable class


let navbar = document.getElementById("site-nav");
let lang_button = document.createElement("a");

const params = new URLSearchParams(window.location.search);
const lang = params.get("lang") || "es"; // set spanish as default

const base_url = location.origin + location.pathname;

// change all links on the page to point to the correct language
if(lang != "es"){
    const link_elements = document.getElementsByTagName("a");
    Array.from(link_elements).forEach(elem => {
        elem.href = elem.href + "?lang=" + lang;
    })
}

lang_button.id = "lang-button";
if(lang == "es"){
    lang_button.innerHTML = "English";
    lang_button.href = base_url + "?lang=en";
}else{
    lang_button.innerHTML = "Español";
    lang_button.href = base_url; // could also be + "?lang=es" but this is cleaner
}

navbar.appendChild(lang_button);

// only change the contents if is is not english, since that is what it was written in
if(lang != "en"){
    // pull the translation file
    fetch(`/lang/${lang}.json`).then(res => {
        res.json().then(translations => {
            // once json has been processed, find the meastead list items, and them change the text of their links
            let elems = document.getElementsByClassName("masthead__menu-item"); 
            Array.from(elems).forEach(elem => {
                // access the <a> element of each <li> element
                // entries in the translation JSON should be labeled with the original page name
                elem.children[0].innerHTML = translations[elem.children[0].innerHTML];
            });
        })
    })

    // Fill in page for the selected language
    changeLanguage(lang);
}

async function changeLanguage(lang) {
    // pull and process the translation file
    const res = await fetch(`/lang/${lang}.json`);
    // why is this processed again?
    const translations = await res.json();

    // since the map title is the autogenerated title of the page, we must hunt for the element
    if(location.pathname=="/map/"){
        let title_div = document.getElementById("page-title");
        title_div.children[0].innerText = translations["Map Title"];
    }

    // change the team bio if its there
    const bio_div = document.getElementsByClassName("author__bio")[0];
    if(bio_div && translations["author-bio"]){
        bio_div.children[0].innerText = translations["author-bio"];
    }

    // change the titles and summaries of all the project description pages
    if(location.pathname=="/project/"){
        const posts = document.getElementsByClassName("archive__item");

        Array.from(posts).forEach(elem => {
            const title = elem.children[0].children[0].innerText;
            elem.children[0].children[0].innerText = translations[title];
            elem.children[1].innerText = translations[title + "-summary"];
        })
    }

    // change the page title
    const title = document.getElementById("page-title");
    if(title && translations[title.innerText]){
        title.innerText = translations[title.innerText];
    }

    // dynamically translate elements by id
    let elems = document.getElementsByClassName("translatable");
    Array.from(elems).forEach(elem => {
        elem.innerHTML = translations[elem.id];
    });
}

// also sneaky change the link to the Energy Usage Tips to open the PDF in a new tab
let elems = document.getElementsByClassName("masthead__menu-item");
Array.from(elems).forEach(elem => {
    // access the <a> element of each <li> element
    // if the link is pointing to the brochure, we know that is the one we want to change
    if(elem.children[0].href == window.location.origin + "/assets/data/Puerto%20Real%20Energy%20Brochure.pdf"){
        elem.children[0].target = "_blank";
    }
});

// also also super sneaky add the favicon to the page, since it is not included in the header
let link = document.createElement("link");
link.rel = "icon";
link.href = "/assets/images/logo.png";
document.head.appendChild(link);

// and get rid of the Recent Posts header on the index page
const recent_posts = document.getElementsByClassName("archive__subtitle")[0];
if(recent_posts){
    recent_posts.remove();
}